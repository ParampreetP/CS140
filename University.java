package assignment01;

public class University {

	private String name; // This is the University name
	private String city; // This is the university city

	public University(String uniname, String unicity) {
		name = uniname;
		city = unicity;
	}

	public String getname() {
		return name;
	}

	public String getcity() {
		return city;
	}

	public String getName() {
		return name;
	}

	public String getCity() {
		return city;
	}

}
